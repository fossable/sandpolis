pub struct DisplayData {
    /// The display's name
    pub name: String,
    /// null
    pub edid: String,
    /// The display's resolution
    pub resolution: String,
    /// The display's physical size in pixels
    pub size: String,
    /// Refresh frequency in Hertz
    pub refresh_frequency: u32,
    /// null
    pub bit_depth: u32,
}
